    private void resetAll() {
        codeField.setEditable(false);
        titleField.setEditable(false);
        dopField.setEditable(false);
        newBtn.setEnabled(true);
        editBtn.setEnabled(false);
        saveBtn.setEnabled(false);
        codeField.setText("");
        titleField.setText("");
        dopField.setText("");
    }

    private void newPressed() {
        codeField.setEditable(true);
        titleField.setEditable(true);
        dopField.setEditable(true);
        newBtn.setEnabled(false);
        editBtn.setEnabled(false);
        saveBtn.setEnabled(true);
        list.clearSelection();
        current = null;
        codeField.setText("");
        titleField.setText("");
        dopField.setText("");
        codeField.requestFocus();
        status.setText("");
        status.setForeground(Color.blue);        
    }

    private void editPressed() {
        codeField.setEditable(false);
        titleField.setEditable(true);
        dopField.setEditable(true);
        newBtn.setEnabled(true);
        editBtn.setEnabled(false);
        saveBtn.setEnabled(true);
        titleField.requestFocus();
    }

    private void savePressed() {
        codeField.setEditable(false);
        titleField.setEditable(false);
        dopField.setEditable(false);
        newBtn.setEnabled(true);
        editBtn.setEnabled(true);
        saveBtn.setEnabled(false);
    }

    private void selectionPerformed() {
        codeField.setEditable(false);
        titleField.setEditable(false);
        dopField.setEditable(false);
        newBtn.setEnabled(true);
        editBtn.setEnabled(true);
        saveBtn.setEnabled(false);
    }

    private boolean validateFields() {
        String scode = codeField.getText();
        if (Objects.isNull(codeField) || scode.trim().isEmpty()) {
            status.setForeground(Color.red);
            status.setText("Should provide a Code!");
            return false;
        }
        String stitle = titleField.getText();
        if (Objects.isNull(stitle) || stitle.trim().isEmpty()) {
            status.setForeground(Color.red);
            status.setText("Should provide a Title!");
            return false;
        }
        String sdop = dopField.getText();
        if (Objects.isNull(sdop) || sdop.trim().isEmpty()) {
            status.setForeground(Color.red);
            status.setText("Should provide a Date!");
            return false;
        } else {
            try {
                LocalDate.parse(sdop);
            } catch (DateTimeParseException ex) {
                status.setForeground(Color.red);
                status.setText("Should provide a Correct Date!");
                return false;
            }
        }
        return true;
    }

