    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        savePressed();
        //Validation
        if (!validateFields()) {
            editPressed();
            return;
        }
        if (!list.isSelectionEmpty()) {
            current.setTitle(titleField.getText().trim());
            current.setDop(LocalDate.parse(dopField.getText().trim()));
            controller.update(current, new BookCallback());
        } else {
            current = new Book(codeField.getText().trim(), titleField.getText().trim(), LocalDate.parse(dopField.getText().trim()));
            controller.create(current, new BookCallback());
            listModel.add(listModel.getSize(), current);
            list.setSelectedIndex(listModel.getSize() - 1);
        }

    }//GEN-LAST:event_saveBtnActionPerformed

    private void listValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listValueChanged
        if (!list.isSelectionEmpty()) {
            selectionPerformed();
            current = list.getSelectedValue();
            codeField.setText(current.getCode());
            titleField.setText(current.getTitle());
            dopField.setText(current.getDop().toString());
        }
    }//GEN-LAST:event_listValueChanged

